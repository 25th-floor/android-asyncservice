package com.joanzapata.android.asyncservice.demo.event;

import java.io.Serializable;

public class UserEvent implements Serializable {

    private Long id;

    private String name;

    private int age;

    UserEvent() {}

    public UserEvent(Long id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
